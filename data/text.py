def hit_wall(direction: str) -> str:
    action = {
        "left": "walked to the left",
        "right": "walked to the right",
        "forward": "walked forward",
        "back": "turned back",
    }
    return f"You {action[direction]} and smashed into a wall"

def loot_no(name: str) -> str:
    return f"You left {name} on the ground and allowed the resourceful rat to steal it"

def loot_other(name: str) -> str:
    return f"Your indecisiveness allowed the resourceful rat to steal the {name} when you weren't looking"

def spell_fail(name: str) -> str:
    return f"You tried to cast {name} but it blew up in your face"

def flask_fail(name: str) -> str:
    return f"You tried drinking {name} but nothing happened"

def flask_succeed(name: str, effect: str) -> str:
    return f"You drank {name} and gained {effect}"

def out_of_item(name: str) -> str:
    return f"You ran out of {name}"

def use_fail(name: str) -> str:
    return f"You tried to use {name} but nothing happened"

def equip_fail(name: str) -> str:
    return f"You tried changing your {name} but nothing happened"

def equip_nonexistent(name: str) -> str:
    return f"You tried equipping {name} but realised you cant create things out of thin air"

def equip_succeed(name: str) -> str:
    return f"You equipped {name}"

def do_not_have(name: str) -> str:
    return f"You do not own any {name}"

def action_fail(action: str) -> str:
    return f"You do not have the physical and mental capability to {action}"

def move_fail(direction: str) -> str:
    return f"You do not know what direction {direction} is and got confused"

def obtain_thing(name: str, type: str) -> str:
    return f"You obtained {name}, a powerful {type}"